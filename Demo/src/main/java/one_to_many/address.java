package one_to_many;

public class address {

	int aid;
	int eid;
	String address;

	public int getAid() {
		return aid;
	}

	public int getEid() {
		return eid;
	}

	public String getAddress() {
		return address;
	}

	public void setAid(int aid) {
		this.aid = aid;
	}

	public void setEid(int eid) {
		this.eid = eid;
	}

	public void setAddress(String address) {
		this.address = address;
	}

}
